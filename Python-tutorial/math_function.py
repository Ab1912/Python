import math # for importing maths functions

print("Squareroot : ",math.sqrt(4))
print("Ceiling value : ",math.ceil(1.4587))
print("Floor value : ",math.floor(1.4587))
print("Factorial : ",math.factorial(5)) # 5*4*3*2*1
print("Absolute number : ",math,abs(-5)) # To display positive number
print("Power function : ",math.pow(2,3))
print("Log function : ",math.log(10))
print("Pi : ",math.pi)



